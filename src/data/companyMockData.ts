export const company_detail_seed = (company_id: string): CompanyDetailRecord | null => {
  const company = company_records_seed.find((record) => record.id === company_id);

  if (!company) {
    return null;
  }

  return {
    company,
    contacts: company_contacts_seed.filter((record) => record.company_id === company_id),
    opportunities: company_opportunities_seed.filter((record) => record.company_id === company_id),
    leads: [],
    activities: company_activities_seed.filter((record) => record.company_id === company_id),
    documents: company_documents_seed.filter((record) => record.company_id === company_id),
    ai_insight: company_ai_insights_seed.find((record) => record.company_id === company_id) ?? null,
  };
};

