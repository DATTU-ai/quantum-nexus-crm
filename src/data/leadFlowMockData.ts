export const createLeadFlowBoardState = (leads: LeadFlowLead[]): LeadFlowBoardState =>
  leadFlowStages.reduce(
    (board, stage) => {
      board[stage] = leads.filter((lead) => lead.stage === stage);
      return board;
    },
    {} as LeadFlowBoardState,
  );

export const leadFlowSeedBoard = createLeadFlowBoardState(leadFlowSeedLeads);

export const buildLeadFlowLead = (input: CreateLeadFlowLeadInput): LeadFlowLead => ({
  id: `LF-${crypto.randomUUID().slice(0, 8).toUpperCase()}`,
  stage: input.stage,
  leadName: input.leadName.trim(),
  companyName: input.companyName.trim(),
  contactPerson: input.contactPerson.trim(),
  location: input.location.trim(),
  rating: input.rating,
  email: input.email.trim(),
  phone: input.phone.trim(),
  dealValue: Math.max(0, Math.round(input.dealValue)),
  temperature: input.temperature,
  status: input.status,
});

export const getLeadStageIndex = (stage: LeadFlowStage) => leadFlowStages.indexOf(stage);

export const cloneLeadFlowBoard = (board: LeadFlowBoardState): LeadFlowBoardState =>
  leadFlowStages.reduce(
    (nextBoard, stage) => {
      nextBoard[stage] = [...board[stage]];
      return nextBoard;
    },
    {} as LeadFlowBoardState,
  );

export const getLeadFlowTotals = (board: LeadFlowBoardState, stage: LeadFlowStage) => ({
  count: board[stage].length,
  value: board[stage].reduce((sum, lead) => sum + lead.dealValue, 0),
});
